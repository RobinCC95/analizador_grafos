class Node:
    def __init__(self,id,dato):
        self.id = id
        self.dato = dato
        
    def get_id(self):
        return self.id
        
    def get_dato(self):
        return self.dato

    def set_id(self,id):
        self.id = id

    def set_dato(self,dato):
        self.dato = dato